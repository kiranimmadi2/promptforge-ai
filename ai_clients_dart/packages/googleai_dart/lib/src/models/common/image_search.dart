/// Image search configuration.
class ImageSearch {
  /// Creates an [ImageSearch].
  const ImageSearch();

  /// Creates an [ImageSearch] from JSON.
  // ignore: avoid_unused_constructor_parameters
  factory ImageSearch.fromJson(Map<String, dynamic> json) =>
      const ImageSearch();

  /// Converts to JSON.
  Map<String, dynamic> toJson() => {};

  /// Creates a copy with replaced values.
  ImageSearch copyWith() => const ImageSearch();

  @override
  String toString() => 'ImageSearch()';
}
