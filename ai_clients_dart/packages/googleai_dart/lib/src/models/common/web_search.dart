/// Web search configuration.
class WebSearch {
  /// Creates a [WebSearch].
  const WebSearch();

  /// Creates a [WebSearch] from JSON.
  // ignore: avoid_unused_constructor_parameters
  factory WebSearch.fromJson(Map<String, dynamic> json) => const WebSearch();

  /// Converts to JSON.
  Map<String, dynamic> toJson() => {};

  /// Creates a copy with replaced values.
  WebSearch copyWith() => const WebSearch();

  @override
  String toString() => 'WebSearch()';
}
