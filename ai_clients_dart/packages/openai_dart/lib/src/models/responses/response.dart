/// A response from the Responses API.
///
/// Contains the model's output along with metadata about the response.
@immutable
class Response {
  /// Unique identifier for the response.
  final String id;

  /// Object type, always 'response'.
  final String object;

  /// Unix timestamp of when the response was created.
  final int createdAt;

  /// The status of the response.
  final ResponseStatus status;

  /// The output items generated by the model.
  final List<OutputItem> output;

  /// Token usage statistics.
  final ResponseUsage? usage;

  /// Error details if the response failed.
  final ResponseError? error;

  /// Details about why the response is incomplete.
  final IncompleteDetails? incompleteDetails;

  /// The model used to generate the response.
  final String? model;

  /// The instructions provided to the model.
  final String? instructions;

  /// The ID of the previous response this continues from.
  final String? previousResponseId;

  /// The service tier used for the request.
  final ServiceTier? serviceTier;

  /// Custom metadata from the request.
  final Map<String, String>? metadata;

  /// The maximum number of output tokens requested.
  final int? maxOutputTokens;

  /// The temperature used for sampling.
  final double? temperature;

  /// The top_p value used for nucleus sampling.
  final double? topP;

  /// Whether the response is a background task.
  final bool? background;

  /// Whether parallel tool calls were allowed.
  final bool? parallelToolCalls;

  /// The prompt cache key for this response.
  final String? promptCacheKey;

  /// The prompt cache retention policy.
  final PromptCacheRetention? promptCacheRetention;

  /// Creates a [Response].
  const Response({
    required this.id,
    required this.object,
    required this.createdAt,
    required this.status,
    required this.output,
    this.usage,
    this.error,
    this.incompleteDetails,
    this.model,
    this.instructions,
    this.previousResponseId,
    this.serviceTier,
    this.metadata,
    this.maxOutputTokens,
    this.temperature,
    this.topP,
    this.background,
    this.parallelToolCalls,
    this.promptCacheKey,
    this.promptCacheRetention,
  });

  /// Creates a [Response] from JSON.
  factory Response.fromJson(Map<String, dynamic> json) {
    return Response(
      id: json['id'] as String,
      object: json['object'] as String,
      createdAt: json['created_at'] as int,
      status: ResponseStatus.fromJson(json['status'] as String),
      output: (json['output'] as List)
          .map((e) => OutputItem.fromJson(e as Map<String, dynamic>))
          .toList(),
      usage: json['usage'] != null
          ? ResponseUsage.fromJson(json['usage'] as Map<String, dynamic>)
          : null,
      error: json['error'] != null
          ? ResponseError.fromJson(json['error'] as Map<String, dynamic>)
          : null,
      incompleteDetails: json['incomplete_details'] != null
          ? IncompleteDetails.fromJson(
              json['incomplete_details'] as Map<String, dynamic>,
            )
          : null,
      model: json['model'] as String?,
      instructions: json['instructions'] as String?,
      previousResponseId: json['previous_response_id'] as String?,
      serviceTier: json['service_tier'] != null
          ? ServiceTier.fromJson(json['service_tier'] as String)
          : null,
      metadata: (json['metadata'] as Map?)?.cast<String, String>(),
      maxOutputTokens: json['max_output_tokens'] as int?,
      temperature: (json['temperature'] as num?)?.toDouble(),
      topP: (json['top_p'] as num?)?.toDouble(),
      background: json['background'] as bool?,
      parallelToolCalls: json['parallel_tool_calls'] as bool?,
      promptCacheKey: json['prompt_cache_key'] as String?,
      promptCacheRetention: json['prompt_cache_retention'] != null
          ? PromptCacheRetention.fromJson(
              json['prompt_cache_retention'] as String,
            )
          : null,
    );
  }

  /// Converts to JSON.
  Map<String, dynamic> toJson() => {
    'id': id,
    'object': object,
    'created_at': createdAt,
    'status': status.toJson(),
    'output': output.map((e) => e.toJson()).toList(),
    if (usage != null) 'usage': usage!.toJson(),
    if (error != null) 'error': error!.toJson(),
    if (incompleteDetails != null)
      'incomplete_details': incompleteDetails!.toJson(),
    if (model != null) 'model': model,
    if (instructions != null) 'instructions': instructions,
    if (previousResponseId != null) 'previous_response_id': previousResponseId,
    if (serviceTier != null) 'service_tier': serviceTier!.toJson(),
    if (metadata != null) 'metadata': metadata,
    if (maxOutputTokens != null) 'max_output_tokens': maxOutputTokens,
    if (temperature != null) 'temperature': temperature,
    if (topP != null) 'top_p': topP,
    if (background != null) 'background': background,
    if (parallelToolCalls != null) 'parallel_tool_calls': parallelToolCalls,
    if (promptCacheKey != null) 'prompt_cache_key': promptCacheKey,
    if (promptCacheRetention != null)
      'prompt_cache_retention': promptCacheRetention!.toJson(),
  };

  // ============================================================
  // Convenience Getters
  // ============================================================

  /// Returns all text content from message output items.
  ///
  /// Concatenates all text from output text content blocks.
  String get outputText {
    final buffer = StringBuffer();
    for (final item in output) {
      if (item is MessageOutputItem) {
        for (final content in item.content) {
          if (content is OutputTextContent) {
            buffer.write(content.text);
          }
        }
      }
    }
    return buffer.toString();
  }

  /// Returns all function call output items.
  List<FunctionCallOutputItemResponse> get functionCalls {
    return output.whereType<FunctionCallOutputItemResponse>().toList();
  }

  /// Whether the response contains any function calls.
  bool get hasToolCalls => functionCalls.isNotEmpty;

  /// Returns all reasoning items from the output.
  List<ReasoningItem> get reasoningItems {
    return output.whereType<ReasoningItem>().toList();
  }

  /// Returns all web search call items from the output.
  List<WebSearchCallOutputItem> get webSearchCalls {
    return output.whereType<WebSearchCallOutputItem>().toList();
  }

  /// Returns all file search call items from the output.
  List<FileSearchCallOutputItem> get fileSearchCalls {
    return output.whereType<FileSearchCallOutputItem>().toList();
  }

  /// Returns all code interpreter call items from the output.
  List<CodeInterpreterCallOutputItem> get codeInterpreterCalls {
    return output.whereType<CodeInterpreterCallOutputItem>().toList();
  }

  /// Returns all image generation call items from the output.
  List<ImageGenerationCallOutputItem> get imageGenerationCalls {
    return output.whereType<ImageGenerationCallOutputItem>().toList();
  }

  /// Returns all shell call items from the output.
  List<ShellCallOutputItem> get shellCalls {
    return output.whereType<ShellCallOutputItem>().toList();
  }

  /// Returns all shell call output items from the output.
  List<ShellCallOutputResultItem> get shellCallOutputs {
    return output.whereType<ShellCallOutputResultItem>().toList();
  }

  /// Returns all local shell call items from the output.
  List<LocalShellCallOutputItem> get localShellCalls {
    return output.whereType<LocalShellCallOutputItem>().toList();
  }

  /// Returns all local shell call output items from the output.
  List<LocalShellCallOutputResultItem> get localShellCallOutputs {
    return output.whereType<LocalShellCallOutputResultItem>().toList();
  }

  /// Returns all compaction items from the output.
  List<CompactionOutputItem> get compactionItems {
    return output.whereType<CompactionOutputItem>().toList();
  }

  /// Returns all MCP call items from the output.
  List<McpCallOutputItem> get mcpCalls {
    return output.whereType<McpCallOutputItem>().toList();
  }

  /// Returns all tool search call items from the output.
  List<ToolSearchCallOutputItem> get toolSearchCalls {
    return output.whereType<ToolSearchCallOutputItem>().toList();
  }

  /// Returns all tool search output items from the output.
  List<ToolSearchOutputItem> get toolSearchOutputs {
    return output.whereType<ToolSearchOutputItem>().toList();
  }

  /// Returns all computer call items from the output.
  List<ComputerCallOutputItem> get computerCalls {
    return output.whereType<ComputerCallOutputItem>().toList();
  }

  /// Whether the response is complete.
  bool get isComplete => status == ResponseStatus.completed;

  /// Whether the response failed.
  bool get isFailed => status == ResponseStatus.failed;

  /// Whether the response is still in progress.
  bool get isInProgress => status == ResponseStatus.inProgress;

  @override
  bool operator ==(Object other) =>
      identical(this, other) ||
      other is Response &&
          runtimeType == other.runtimeType &&
          id == other.id &&
          object == other.object &&
          createdAt == other.createdAt &&
          status == other.status &&
          listsEqual(output, other.output) &&
          usage == other.usage &&
          error == other.error &&
          incompleteDetails == other.incompleteDetails &&
          model == other.model &&
          instructions == other.instructions &&
          previousResponseId == other.previousResponseId &&
          serviceTier == other.serviceTier &&
          mapsEqual(metadata, other.metadata) &&
          maxOutputTokens == other.maxOutputTokens &&
          temperature == other.temperature &&
          topP == other.topP &&
          background == other.background &&
          parallelToolCalls == other.parallelToolCalls &&
          promptCacheKey == other.promptCacheKey &&
          promptCacheRetention == other.promptCacheRetention;

  @override
  int get hashCode => Object.hashAll([
    id,
    object,
    createdAt,
    status,
    Object.hashAll(output),
    usage,
    error,
    incompleteDetails,
    model,
    instructions,
    previousResponseId,
    serviceTier,
    mapHash(metadata),
    maxOutputTokens,
    temperature,
    topP,
    background,
    parallelToolCalls,
    promptCacheKey,
    promptCacheRetention,
  ]);

  @override
  String toString() => 'Response(id: $id, status: $status, output: $output)';
}

/// A list of responses with pagination.
@immutable
class ResponseList {
  /// The list of responses.
  final List<Response> data;

  /// The object type, always 'list'.
  final String object;

  /// Whether there are more responses to fetch.
  final bool hasMore;

  /// The ID of the first response in the list.
  final String? firstId;

  /// The ID of the last response in the list.
  final String? lastId;

  /// Creates a [ResponseList].
  const ResponseList({
    required this.data,
    required this.object,
    required this.hasMore,
    this.firstId,
    this.lastId,
  });

  /// Creates a [ResponseList] from JSON.
  factory ResponseList.fromJson(Map<String, dynamic> json) {
    return ResponseList(
      data: (json['data'] as List)
          .map((e) => Response.fromJson(e as Map<String, dynamic>))
          .toList(),
      object: json['object'] as String,
      hasMore: json['has_more'] as bool,
      firstId: json['first_id'] as String?,
      lastId: json['last_id'] as String?,
    );
  }

  /// Converts to JSON.
  Map<String, dynamic> toJson() => {
    'data': data.map((e) => e.toJson()).toList(),
    'object': object,
    'has_more': hasMore,
    if (firstId != null) 'first_id': firstId,
    if (lastId != null) 'last_id': lastId,
  };

  @override
  bool operator ==(Object other) =>
      identical(this, other) ||
      other is ResponseList &&
          runtimeType == other.runtimeType &&
          listsEqual(data, other.data) &&
          object == other.object &&
          hasMore == other.hasMore &&
          firstId == other.firstId &&
          lastId == other.lastId;

  @override
  int get hashCode =>
      Object.hash(Object.hashAll(data), object, hasMore, firstId, lastId);

  @override
  String toString() =>
      'ResponseList(data: ${data.length} items, hasMore: $hasMore)';
}

/// Response for a delete operation.
@immutable
class DeleteResponseResult {
  /// The ID of the deleted response.
  final String id;

  /// The object type, always 'response.deleted'.
  final String object;

  /// Whether the deletion was successful.
  final bool deleted;

  /// Creates a [DeleteResponseResult].
  const DeleteResponseResult({
    required this.id,
    required this.object,
    required this.deleted,
  });

  /// Creates a [DeleteResponseResult] from JSON.
  factory DeleteResponseResult.fromJson(Map<String, dynamic> json) {
    return DeleteResponseResult(
      id: json['id'] as String,
      object: json['object'] as String,
      deleted: json['deleted'] as bool,
    );
  }

  /// Converts to JSON.
  Map<String, dynamic> toJson() => {
    'id': id,
    'object': object,
    'deleted': deleted,
  };

  @override
  bool operator ==(Object other) =>
      identical(this, other) ||
      other is DeleteResponseResult &&
          runtimeType == other.runtimeType &&
          id == other.id &&
          object == other.object &&
          deleted == other.deleted;

  @override
  int get hashCode => Object.hash(id, object, deleted);

  @override
  String toString() => 'DeleteResponseResult(id: $id, deleted: $deleted)';
}
